package modulo02_estruturasCondicionais;

public class aula01_expressoesComparativas {
    public static void main(String[] args) {
        int pao = 10;
        int acucar = 20;

        System.out.println(pao == acucar);
        System.out.println(pao != acucar);
        System.out.println(pao > acucar);
        System.out.println(pao < acucar);
        System.out.println(pao >= acucar);
        System.out.println(pao <= acucar);
    }
}

